# This program asks for a users first and last name, as well as age. Then returns the user's age next year.
# This line asks for the last name.
last = input('What is your last name?')

# This line asks for the first name.
first = input('What is your first name?')

# This line asks for the user's age.
age = int(input('How old are you?'))

# This gives the output statement with name and age next year.
print(first, last, 'will be', age+1, 'years old next year.')
